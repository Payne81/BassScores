/*
	在C++中，内存分成5个区，他们分别是堆、栈、自由存储区、全局/静态存储区和常量存储区。

	栈，在执行函数时，函数内局部变量的存储单元都可以在栈上创建，函数执行结束时这些存储单
		元自动被释放。栈内存分配运算内置于处理器的指令集中，效率很高，但是分配的内存容量有限。
	
	堆，就是那些由new分配的内存块，他们的释放编译器不去管，由我们的应用程序去控制，一般一
		个new就要对应一个delete。如果程序员没有释放掉，那么在程序结束后，操作系统会自动回收。

	自由存储区，就是那些由malloc等分配的内存块，他和堆是十分相似的，不过它是用free来结束自己的生命的。
	
	全局/静态存储区，全局变量和静态变量被分配到同一块内存中，在以前的C语言中，全局变量又分为初始
		化的和未初始化的，在C++里面没有这个区分了，他们共同占用同一块内存区。

	常量存储区，这是一块比较特殊的存储区，他们里面存放的是常量，不允许修改
  */